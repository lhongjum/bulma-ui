<template>
  <button :class="classes">
    <slot></slot>
  </button>
</template>

<script>
export default {
  name: 'VbDelete',
  props: {
    size: {
      type: String,
      validator: value => ['small', 'medium', 'large'].includes(value)
    }
  },
  computed: {
    classes() {
      const { size } = this
      const obj = {
        delete: true,
        [`is-${size}`]: !!size
      }
      return obj
    }
  }
}
</script>

